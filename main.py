#
# ШАГ 2: Очистка строки от лишних символов
#    - Удалить все символы, которые не являются буквами или цифрами
#    - Пробелы, знаки препинания, специальные символы игнорируются
#
# ШАГ 3: Получение обратной версии строки
#    - Создать копию очищенной строки, развернутую в обратном порядке
#
# ШАГ 4: Сравнение строк
#    - Сравнить очищенную исходную строку с её обратной версией
#
# ШАГ 5: Возврат результата
#    - Если строки идентичны: вернуть True (палиндром)
#    - Если строки отличаются: вернуть False (не палиндром)

def is_palindrome(s):
    s_lower = s.lower()
    cleaned = ''.join(char for char in s_lower if char.isalnum())
    return cleaned == cleaned[::-1]

word = "топот"
print(is_palindrome(word))